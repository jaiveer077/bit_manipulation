#include<bits/stdc++.h>
using namespace std;
#define ll long long
int main(){
    ll x=13&7;  //  bitwise AND 
    cout<<x<<endl;
    ll y=13|7; // bitwise OR
    cout<<y<<endl;
    ll z=13^7; // bitwise XOR 
    cout<<z<<endl;
    ll a=13>>4; // right shift  
    cout<<a<<endl;
    ll b=13<<1;
    cout<<b<<endl; //left shift 
    return 0;
}